const sum_function = (a, b) => {
  return a + b;
};

const subtract_function = (a, b) => {
  return a - b;
};

const multiply_function = (a, b) => {
  return a * b;
};

const divide_function = (a, b) => {
  return a / b;
};

module.exports = {
  sum: sum_function,
  subtract: subtract_function,
  multiply: multiply_function,
  divide: divide_function
};
